do{
    console.log("CFB curso")
    n++
}while (n<10)
console.log("fim do programa")


/*
Em resumo, o laço do...while executa o bloco de código dentro do do uma vez, independentemente da condição do while, e depois verifica a condição. Se a condição for falsa, o laço termina e o programa continua executando o código após o laço.

No caso deste código, como a condição n < 10 é falsa desde o início (pois n é inicializado com 10), o laço do...while só executa o bloco de código dentro do do uma vez e depois termina.
*/