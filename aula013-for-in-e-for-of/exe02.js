// Declaração de uma variável "num" que é um array de números
let num = [10, 20, 30, 40, 50];

// Loop "for...in" que itera sobre as chaves do array "num"
// "n" é a variável que assume o valor da chave (índice) de cada elemento do array em cada iteração
for (n in num) {
  // Imprime o valor do elemento do array que está na posição "n"
  console.log(num[n]);
}