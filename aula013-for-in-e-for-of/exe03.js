// Declaração de uma variável "num" que é um array de números
let num = [10, 20, 30, 40, 50];

// Loop "for...of" que itera sobre o array "num"
// "n" é a variável que assume o valor de cada elemento do array em cada iteração
for (n of num) {
  // Imprime o valor de "n" no console
  console.log(n);
}